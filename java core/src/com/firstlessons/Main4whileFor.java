package com.firstlessons;

public class Main4whileFor {
    public static void main(String[] args) {
        int i = 0;
        while (i < 3) {
            System.out.println(i);
            i++;
        }
        //fori
        for (int j = 0; j < 3; j++) {
            System.out.println(j);
        }

        int k = 0;
        do {
            System.out.println(k);
            k++;
        } while (k<3);
    }
}
